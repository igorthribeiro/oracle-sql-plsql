create or replace PROCEDURE SP_GRAVA_LOG(P_OPERACAO SISLOG.DS_OPERACAO%TYPE)
IS PRAGMA AUTONOMOUS_TRANSACTION;
  V_USER VARCHAR2(40 CHAR);
  V_HOST VARCHAR2(255 CHAR);
  V_DATA VARCHAR2(40 CHAR);
BEGIN
   V_USER := USER;
   V_HOST := SYS_CONTEXT('USERENV','HOST');
   V_DATA := TO_CHAR(SYSDATE, 'DD/MM/YYYY - HH24:MI:SS');

   INSERT INTO SISLOG(CD_SISLOG, NM_USUARIO, NM_HOST, DS_DATA_HORA, DS_OPERACAO)
          VALUES (SQ_CD_SISLOG.NEXTVAL, V_USER, V_HOST, V_DATA, P_OPERACAO);
   COMMIT;

END;