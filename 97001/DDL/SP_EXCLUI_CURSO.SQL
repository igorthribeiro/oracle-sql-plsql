create or replace PROCEDURE SP_EXCLUI_CURSO (P_CD_CURSO CURSO.CD_CURSO%TYPE,
                                             P_FEEDBACK OUT VARCHAR2) 
IS
  E_CURSO_INEXISTENTE EXCEPTION;
BEGIN
    DELETE CURSO WHERE CD_CURSO = P_CD_CURSO;

    IF SQL%ROWCOUNT = 0 THEN
       RAISE E_CURSO_INEXISTENTE;
    END IF;
    P_FEEDBACK := 'CURSO EXCLUIDO COM SUCESSO!';
    
    EXCEPTION
       WHEN E_CURSO_INEXISTENTE THEN
          P_FEEDBACK := 'O CURSO N√ÉO EXISTE NA TABELA';    
END;