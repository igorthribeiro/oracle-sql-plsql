create or replace PROCEDURE SP_INSERE_ALUNO_NOTAS(
                                 P_NM_ALUNO ALUNO.NM_NOME%TYPE,
                                 P_DS_EMAIL ALUNO.DS_EMAIL%TYPE,
                                 P_NN_NOTA1 NOTAS.NN_NOTA1%TYPE,
                                 P_NN_NOTA2 NOTAS.NN_NOTA2%TYPE,
                                 P_NN_NOTA3 NOTAS.NN_NOTA3%TYPE,
                                 P_NN_NOTA4 NOTAS.NN_NOTA4%TYPE)
IS
   V_SQL_INSERE_ALUNO VARCHAR2(500);
   V_SQL_INSERE_NOTAS VARCHAR2(500);
BEGIN
   V_SQL_INSERE_ALUNO := 'BEGIN PKG_TREINAMENTO.SP_INSERE_ALUNO(:1, :2); END;';
   EXECUTE IMMEDIATE V_SQL_INSERE_ALUNO USING P_NM_ALUNO, P_DS_EMAIL;
   DBMS_OUTPUT.PUT_LINE('RESULTADO: ' || PKG_TREINAMENTO.V_SUCESSO);

   V_SQL_INSERE_NOTAS := 'BEGIN PKG_TREINAMENTO.SP_INSERE_NOTAS(:1, :2, :3, :4, :5); END;';
   EXECUTE IMMEDIATE V_SQL_INSERE_NOTAS USING SQ_CD_ALUNO.CURRVAL, P_NN_NOTA1, P_NN_NOTA2,
                                                                   P_NN_NOTA3, P_NN_NOTA4;
   DBMS_OUTPUT.PUT_LINE('RESULTADO: ' || PKG_TREINAMENTO.V_SUCESSO);
END;